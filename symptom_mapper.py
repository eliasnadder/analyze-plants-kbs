def map_text_to_symptoms(
    user_text: str,
    crop_symptoms: list[str] | None = None,
) -> list[str]:

    clean_text = normalize_arabic_text(user_text)
    found: list[str] = []

    for official_name, keywords in SYMPTOM_KEYWORDS.items():

        if crop_symptoms and official_name not in crop_symptoms:
            continue

        for kw in keywords:
            if normalize_arabic_text(kw) in clean_text:
                if official_name not in found:
                    found.append(official_name)
                break

    return found