import re

_DIALECT_RULES = [
    (r'\bعالأوراق\b',  'على الأوراق'),
    (r'\bعالورق\b',    'على الأوراق'),
    (r'\bعالثمار\b',   'على الثمار'),
    (r'\bعالثمر\b',    'على الثمار'),
    (r'\bعالجذور\b',   'على الجذور'),
    (r'\bعالساق\b',    'على الساق'),
    (r'\bعالأغصان\b',  'على الأغصان'),
    (r'\bعالأوعية\b',  'على الأوعية'),
    (r'\bعالسطح\b',    'على السطح'),
    (r'\bعالقشرة\b',   'على القشرة'),
    (r'\bعالعناقيد\b', 'على العناقيد'),
    (r'\bعالقاعدة\b',  'على القاعدة'),
    (r'\bبالبطاطا\b',  'في البطاطا'),
    (r'\bبالنبات\b',   'في النبات'),
    (r'\bبالأوراق\b',  'في الأوراق'),
    (r'\bمنالأوراق\b', 'من الأوراق'),

    (r'\bبيوقع\b',      'يتساقط'),
    (r'\bعم يوقع\b',    'يتساقط'),
    (r'\bواقع\b',       'يتساقط'),
    (r'\bخربان\b',      'فاسد متعفن'),
    (r'\bخربانة\b',     'فاسدة متعفنة'),
    (r'\bمضروب\b',      'مصاب'),
    (r'\bمضروبة\b',     'مصابة'),
    (r'\bنشف\b',        'جاف ذبل'),
    (r'\bنشفان\b',      'جاف ذابل'),
    (r'\bداير\b',       'يوجد'),
    (r'\bشايف\b',       'يظهر'),
    (r'\bبيطلع\b',      'يظهر ينمو'),
    (r'\bبتطلع\b',      'تظهر تنمو'),
    (r'\bاصفر\b',       'اصفرار'),        
    (r'\bأصفر\b',       'اصفرار'),
    (r'\bابيض\b',       'ابيض نمو ابيض'),
    (r'\bأبيض\b',       'ابيض نمو ابيض'),
    (r'\bبني\b',        'بني عفن بني'),
    (r'\bبنية\b',       'بنية عفن بني'),
    (r'\bاتحرقت\b',     'تفحم احتراق'),
    (r'\bمية\b',        'مائي رطب'),
    (r'\bميّة\b',       'مائي رطب'),
]


def normalize_arabic_text(text: str) -> str:
    if not text: return ""

    for pattern, replacement in _DIALECT_RULES:
        text = re.sub(pattern, replacement, text, flags=re.IGNORECASE)

    text = re.sub(r'[\u064B-\u065F]', '', text)

    text = re.sub(r'ـ', '', text)

    text = re.sub(r'[أإآ]', 'ا', text)

    text = re.sub(r'ة', 'ه', text)

    text = re.sub(r'ى', 'ي', text)

    text = re.sub(r'[^\w\s]', ' ', text)

    text = re.sub(r'\d+', ' ', text)

    text = re.sub(r'\s+', ' ', text).strip()

    return text
